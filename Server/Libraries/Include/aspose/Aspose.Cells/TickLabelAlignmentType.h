#ifndef ASPOSE_CELLS_CHARTS_TICKLABELALIGNMENTTYPE_H
#define ASPOSE_CELLS_CHARTS_TICKLABELALIGNMENTTYPE_H

namespace Aspose { namespace Cells { namespace Charts {

/// <summary>
/// Represents the text alignment type for the tick labels on the axis
/// </summary>
enum class TickLabelAlignmentType {
    /// <summary>
    /// Represents the text shall be centered.
    /// </summary>
    Center,
    /// <summary>
    /// Represents the text shall be left justified.
    /// </summary>
    Left,
    /// <summary>
    /// Represents the text shall be right justified.
    /// </summary>
    Right,
};

} } }

#endif
