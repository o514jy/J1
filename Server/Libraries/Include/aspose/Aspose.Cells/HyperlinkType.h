#ifndef ASPOSE_CELLS_HYPERLINKTYPE_H
#define ASPOSE_CELLS_HYPERLINKTYPE_H

namespace Aspose { namespace Cells {

/// <summary>
/// Represents the type of hyperlink.
/// </summary>
enum class HyperlinkType {
    /// <summary>
    /// URL
    /// </summary>
    URL = 0,
    /// <summary>
    /// Link to external file.
    /// </summary>
    File = 1,
    /// <summary>
    /// Link the cell of the workbook.
    /// </summary>
    Cell = 2,
    /// <summary>
    /// Unknown type.
    /// </summary>
    Unknown,
};

} }

#endif
